#include "converter.h"

double centimetres_to_millimetres(double centimetres);
{
    double millimetre;
    millimetre = centimetre * MILLIMETRES_IN_CENTIMETRE;
    return millimetre;
}

double metres_to_centimetres(double metres);
{
    double metre;
    metre = centimetre * CENTIMETRES_IN_METRE;
    return metre;
}

